const int md = 1e9+7;
const int mxN = 1e6+6;

int dp[mxN][3]; // dp[i][1] = separate end, dp[i][2] = fused end

signed main() {
    ios_base::sync_with_stdio(false); cin.tie(0); cout.tie(0);

    // Base cases
    dp[1][1] = 1; // separate last blocks
    dp[1][2] = 1; // fused last blocks

    // Fill the table up to maximum possible n
    for (int i = 2; i < mxN; i++) {
        dp[i][1] = (dp[i-1][1] * 4 % md + dp[i-1][2]) % md;
        dp[i][2] = (dp[i-1][1] + dp[i-1][2] * 2 % md) % md;
    }

    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        cout << (dp[n][1] + dp[n][2]) % md << endl;
    }
}